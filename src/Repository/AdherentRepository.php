    public function getAdherentsNonExpires($date)
    {
        return $this->createQueryBuilder('a')
            ->andWhere('a.date_expiration >= :date')
            ->setParameter('date', $date)
			->getQuery()
			->getResult();
    }

    public function getAdherentsExpires($date)
    {
        return $this->createQueryBuilder('a')
            ->andWhere('a.date_expiration < :date')
